const mongoose = require('mongoose');

const contributionSchema = new mongoose.Schema({
  from: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User',
    required: true
  },
  to: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User',
    required: true
  },
  amount: {
    type: Number,
    required: true
  },
  message: String
}, { timestamps: true });

module.exports = mongoose.model('Contribution', contributionSchema);
